	fun getNumberInIndianFormat(amount: String): String  {
	    val stringBuilder = StringBuilder()
	    val amountArray: CharArray = amount.toCharArray()
	    var a: Int = 0
		var b: Int = 0
		var i: Int = amountArray.size;
	    for (ii in amountArray) {
			i = i - 1
	        if (a < 3) {
	            stringBuilder.append(amountArray[i]);
	            a++;
	        } else if (b < 2) {
	            if (b == 0) {
	                stringBuilder.append(",");
	                stringBuilder.append(amountArray[i]);
	                b++;
	            } else {
	                stringBuilder.append(amountArray[i]);
	                b = 0;
	            }
	        }
	    }
	    return stringBuilder.reverse().toString();
	}
	
	fun writeToFile(file: String, value: String) {
		try {
			var fwr = FileWriter(file, true);
			var out = BufferedWriter(fwr);
			out.write(value + "\n");
			out.close();
			fwr.close();
		} catch (e: Exception) {
			System.err.println("Error: " + e.message);
		}
	}
	
